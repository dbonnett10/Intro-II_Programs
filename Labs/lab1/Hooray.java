public class Hooray { //states the name of the class as Hooray

    public static void hooray() {   //the public hooray method

		while(true) {
			System.out.println("Hooray!");  //Prints the word Hooray while the condition in the while loop is true.
    	} //while

    } //hooray

    public static void main(String[] args) {  //main method of the program
		hooray(); //calls on the hooray method.
    } //main

        // This infinitely prints out the word Hooray because the program is an infinite while loop. Meaning that there is no condition that breaks the loop.

} //Hooray (class)
